import { useState, useEffect, useCallback, useRef } from 'react';
import {
  ParkingLogItem,
  HistoryResponse,
  ParkingLogResponse,
} from '../types/Log';
import { WebSocketResponse } from '../types/Common';
import { ProcessedFilters, SearchFields } from '../types/Search';
