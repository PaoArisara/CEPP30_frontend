import { API_CONFIG } from '../config/ApiConfig';
import { useSocketService } from './socket/useSocketService';
import LogSocketService from '../services/LogSocketService';

export const headersConfig = [
  { label: '‡∏ó‡∏∞‡πÄ‡∏ö‡∏µ‡∏¢‡∏ô‡∏£‡∏ñ', key: 'license' },
  { label: '‡∏ï‡∏≥‡πÅ‡∏´‡∏ô‡πà‡∏á‡∏à‡∏≠‡∏î‡∏£‡∏ñ', key: 'location' },
  { label: '‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà‡πÅ‡∏•‡∏∞‡πÄ‡∏ß‡∏•‡∏≤‡πÄ‡∏Ç‡πâ‡∏≤', key: 'timeIn' },
  { label: '‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà‡πÅ‡∏•‡∏∞‡πÄ‡∏ß‡∏•‡∏≤‡∏≠‡∏≠‡∏Å', key: 'timeOut' },
];

const initialFilters: ProcessedFilters = {
  page: 1,
  limit: 10,
};

export const useParkingHistory = () => {
  const { token } = useAuth();

  const socket = useSocketService(LogSocketService, true);
  const [isConnected, setIsConnected] = useState(false);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);
  
  // ‡πÄ‡∏û‡∏¥‡πà‡∏° ref ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÄ‡∏Å‡πá‡∏ö‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡∏Å‡∏≤‡∏£‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡πà‡∏≤‡∏™‡∏∏‡∏î
  const fetchingRef = useRef<boolean>(false);
  // ‡πÄ‡∏û‡∏¥‡πà‡∏° timeout ref ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏Å‡∏±‡∏ö‡∏Å‡∏≤‡∏£‡∏£‡∏≠‡∏Å‡∏≤‡∏£‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠
  const connectionTimeoutRef = useRef<NodeJS.Timeout | null>(null);

  const [logs, setLogs] = useState<ParkingLogResponse>({
    data: [],
    meta: { total: 0, totalPages: 0, page: 1, limit: 10 }
  });

  // UI States
  const [activeMap, setActiveMap] = useState<string>('AB');
  const [lastRefreshTime, setLastRefreshTime] = useState(new Date());
  const [selectedCar, setSelectedCar] = useState<ParkingLogItem | null>(null);
  const [showCarInfoModal, setShowCarInfoModal] = useState(false);
  const [showCarHistoryModal, setShowCarHistoryModal] = useState(false);

  // History Modal States
  const [historyLoading, setHistoryLoading] = useState(false);
  const [history, setHistory] = useState<HistoryResponse | null>(null);

  // Filter States
  const [filters, setFilters] = useState<ProcessedFilters>(initialFilters);

  // Data Fetching
  const fetchParkingData = useCallback(async (map: string) => {
    // ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡∏Å‡∏≥‡∏•‡∏±‡∏á‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏≠‡∏¢‡∏π‡πà‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà
    if (fetchingRef.current) {
      console.log('‡∏Ç‡∏ì‡∏∞‡∏ô‡∏µ‡πâ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏£‡∏≠‡∏™‡∏±‡∏Å‡∏Ñ‡∏£‡∏π‡πà...');
      return;
    }

    // ‡∏•‡πâ‡∏≤‡∏á timeout ‡∏Å‡πà‡∏≠‡∏ô‡∏´‡∏ô‡πâ‡∏≤‡∏ñ‡πâ‡∏≤‡∏°‡∏µ
    if (connectionTimeoutRef.current) {
      clearTimeout(connectionTimeoutRef.current);
      connectionTimeoutRef.current = null;
    }

    if (!socket?.connected) {
      setError('‡∏£‡∏≠‡∏Å‡∏≤‡∏£‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠...');
      
      // ‡∏ï‡∏±‡πâ‡∏á timeout ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏£‡∏≠‡∏Å‡∏≤‡∏£‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à‡∏à‡∏∞‡πÅ‡∏™‡∏î‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏ß‡πà‡∏≤‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ
      connectionTimeoutRef.current = setTimeout(() => {
        if (!socket?.connected) {
          setError('‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡∏Å‡∏±‡∏ö‡πÄ‡∏ã‡∏¥‡∏£‡πå‡∏ü‡πÄ‡∏ß‡∏≠‡∏£‡πå‡πÑ‡∏î‡πâ ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏•‡∏≠‡∏á‡πÉ‡∏´‡∏°‡πà‡∏≠‡∏µ‡∏Å‡∏Ñ‡∏£‡∏±‡πâ‡∏á');
          setLoading(false);
        }
      }, 10000); // ‡∏£‡∏≠ 10 ‡∏ß‡∏¥‡∏ô‡∏≤‡∏ó‡∏µ
      
      return;
    }

    const hasExistingData = logs.data.length > 0;
    if (!hasExistingData) {
      setLoading(true);
    }

    fetchingRef.current = true; // ‡πÄ‡∏£‡∏¥‡πà‡∏°‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
    
    try {
      const queryParams = {
        ...filters,
        zone: map !== 'AB' ? map : null,
        timestamp: new Date().toISOString() // ‡πÄ‡∏û‡∏¥‡πà‡∏° timestamp ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏õ‡πâ‡∏≠‡∏á‡∏Å‡∏±‡∏ô cache
      };

      console.log('‡∏Å‡∏≥‡∏•‡∏±‡∏á‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• parking logs...', queryParams);

      socket.emit('getLogs', queryParams, (response: WebSocketResponse<ParkingLogResponse>) => {
        fetchingRef.current = false; // ‡∏™‡∏¥‡πâ‡∏ô‡∏™‡∏∏‡∏î‡∏Å‡∏≤‡∏£‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
        
        if (response.success && response.data) {
          setLogs(response.data);
          setLastRefreshTime(new Date());
          setError(null); // ‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç: ‡∏•‡πâ‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° error ‡πÄ‡∏°‡∏∑‡πà‡∏≠‡πÇ‡∏´‡∏•‡∏î‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à
        } else {
          setError(response.error || '‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÑ‡∏î‡πâ');
        }
        setLoading(false);
      });
    } catch (err) {
      console.error('Error fetching logs:', err);
      setError('‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•');
      setLoading(false);
      fetchingRef.current = false; // ‡∏™‡∏¥‡πâ‡∏ô‡∏™‡∏∏‡∏î‡∏Å‡∏≤‡∏£‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏°‡∏∑‡πà‡∏≠‡πÄ‡∏Å‡∏¥‡∏î error
    }
  }, [socket, filters, logs.data.length]);

  // WebSocket Event Handlers
  const handleLogUpdate = useCallback((data: any) => {
    console.log('Received log update:', data);

    if (data.success) {
      // ‡∏£‡∏µ‡πÄ‡∏ü‡∏£‡∏ä‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î
      fetchParkingData(activeMap);
      setLastRefreshTime(new Date());
    }
  }, [fetchParkingData, activeMap]);

  // Socket Connection Setup
  useEffect(() => {
    if (!socket) return;

    const handleConnect = () => {
      console.log('üîó Socket connected successfully');
      setIsConnected(true);
      setError(null); // ‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç: ‡∏•‡πâ‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° error ‡πÄ‡∏°‡∏∑‡πà‡∏≠‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à
      
      // ‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏±‡∏ô‡∏ó‡∏µ‡πÄ‡∏°‡∏∑‡πà‡∏≠‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à
      fetchParkingData(activeMap);
    };

    const handleDisconnect = () => {
      console.log('‚ö†Ô∏è Socket disconnected');
      setIsConnected(false);
      setError('‡∏Å‡∏≤‡∏£‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡∏Ç‡∏±‡∏î‡∏Ç‡πâ‡∏≠‡∏á ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏û‡∏¢‡∏≤‡∏¢‡∏≤‡∏°‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡πÉ‡∏´‡∏°‡πà...');
    };

    socket.on('connect', handleConnect);
    socket.on('disconnect', handleDisconnect);
    socket.on('logCreated', handleLogUpdate);
    socket.on('logUpdated', handleLogUpdate);
    socket.on('statsUpdated', handleLogUpdate);

    if (socket.connected) {
      setIsConnected(true);
      setError(null); // ‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç: ‡∏•‡πâ‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏° error ‡∏ñ‡πâ‡∏≤ socket ‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡∏≠‡∏¢‡∏π‡πà‡πÅ‡∏•‡πâ‡∏ß
    }

    return () => {
      // ‡∏•‡πâ‡∏≤‡∏á timeout ‡∏Å‡πà‡∏≠‡∏ô‡∏ó‡∏µ‡πà‡∏à‡∏∞ unmount component
      if (connectionTimeoutRef.current) {
        clearTimeout(connectionTimeoutRef.current);
        connectionTimeoutRef.current = null;
      }
      
      socket.off('connect', handleConnect);
      socket.off('disconnect', handleDisconnect);
      socket.off('logCreated', handleLogUpdate);
      socket.off('logUpdated', handleLogUpdate);
      socket.off('statsUpdated', handleLogUpdate);
    };
  }, [socket, handleLogUpdate, activeMap, fetchParkingData]);

  // ‡∏ó‡∏≥‡∏Å‡∏≤‡∏£‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÉ‡∏´‡∏°‡πà‡∏≠‡∏µ‡∏Å‡∏Ñ‡∏£‡∏±‡πâ‡∏á‡∏´‡∏•‡∏±‡∏á‡∏à‡∏≤‡∏Å token refresh
  useEffect(() => {
    if (token && isConnected) {
      console.log('Token ‡∏°‡∏µ‡∏Å‡∏≤‡∏£‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡πÅ‡∏õ‡∏•‡∏á ‡∏´‡∏£‡∏∑‡∏≠‡∏Å‡∏≤‡∏£‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡πÅ‡∏õ‡∏•‡∏á ‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÉ‡∏´‡∏°‡πà');
      fetchParkingData(activeMap);
    }
  }, [token, isConnected, activeMap, fetchParkingData]);

  // In useParkingHistory.ts
  const getCarHistory = async (
    vehicle_id: string, 
    page: number = 1, 
    limit: number = 10
  ) => {
    console.log('Fetching history data:', { vehicle_id, token, page, limit });
  
    if (!token) {
      setError('Unauthorized: No token available');
      setHistoryLoading(false);
      return;
    }
  
    setHistoryLoading(true);
    try {
      const response = await fetch(
        `${API_CONFIG.baseURL}/vehicles/${vehicle_id}/history?page=${page}&limit=${limit}`, 
        {
          method: 'GET',
          headers: {
            'Authorization': `Bearer ${token}`,
            'Content-Type': 'application/json'
          }
        }
      );
  
      if (!response.ok) {
        // Try to get error message from response
        const errorText = await response.text();
        throw new Error(errorText || 'Failed to fetch vehicle history');
      }
  
      const data = await response.json();
      console.log('Fetched history data:', data);
      
      setHistory(data);
      setError(null);
    } catch (error) {
      console.error('Fetch vehicle history error:', error);
      setError('‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•');
    } finally {
      setHistoryLoading(false);
    }
  };

  // Filter Handlers
  const handleSearch = useCallback((searchParams: SearchFields) => {
    const processedParams = {
      ...searchParams,
      page: 1,
      timestamp_in: searchParams.timestamp_in instanceof Date
        ? searchParams.timestamp_in.toISOString()
        : searchParams.timestamp_in,
      timestamp_out: searchParams.timestamp_out instanceof Date
        ? searchParams.timestamp_out.toISOString()
        : searchParams.timestamp_out,
    };

    setFilters((prev: any) => ({
      ...prev,
      ...processedParams
    }));
  }, []);

  const handleClear = useCallback(() => {
    setFilters({
      page: 1,
      limit: 10,
    });
  }, []);

  // Map Change Handler
  const handleMapChange = useCallback((map: string) => {
    setActiveMap(map);
    fetchParkingData(map);
  }, [fetchParkingData]);

  // Pagination Handler
  const handlePageChange = useCallback((page: number) => {
    setFilters((prev: any) => ({ ...prev, page }));
  }, []);

  // Modal Controls
  const handleCarSelect = useCallback((car: ParkingLogItem, type: 'info' | 'history') => {
    setSelectedCar(car);
    if (type === 'info') {
      setShowCarInfoModal(true);
      setShowCarHistoryModal(false);
    } else {
      setShowCarHistoryModal(true);
      setShowCarInfoModal(false);
      getCarHistory(car.vehicle_id);
    }
  }, [getCarHistory]);

  const closeModals = useCallback(() => {
    setShowCarInfoModal(false);
    setShowCarHistoryModal(false);
    setSelectedCar(null);
  }, []);

  // ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏£‡∏µ‡πÄ‡∏ü‡∏£‡∏ä‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÅ‡∏ö‡∏ö manual
  const refreshData = useCallback(() => {
    if (isConnected) {
      fetchParkingData(activeMap);
    } else {
      // ‡∏û‡∏¢‡∏≤‡∏¢‡∏≤‡∏°‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡πÉ‡∏´‡∏°‡πà‡∏≠‡∏µ‡∏Å‡∏Ñ‡∏£‡∏±‡πâ‡∏á‡∏Å‡πà‡∏≠‡∏ô‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
      if (socket) {
        setError('‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏û‡∏¢‡∏≤‡∏¢‡∏≤‡∏°‡πÄ‡∏ä‡∏∑‡πà‡∏≠‡∏°‡∏ï‡πà‡∏≠‡πÉ‡∏´‡∏°‡πà...');
        socket.connect();
      }
    }
  }, [isConnected, activeMap, fetchParkingData, socket]);

  return {
    // States
    loading,
    isConnected,
    error,
    logs,
    filters,
    activeMap,
    lastRefreshTime,
    selectedCar,
    showCarHistoryModal,
    showCarInfoModal,
    historyLoading,
    history,

    // Handlers
    handleSearch,
    handlePageChange,
    handleClear,
    handleMapChange,
    handleCarSelect,
    closeModals,
    fetchParkingData,
    getCarHistory,
    refreshData // ‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÉ‡∏´‡∏°‡πà‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏£‡∏≠‡∏á‡∏£‡∏±‡∏ö‡∏Å‡∏≤‡∏£‡∏£‡∏µ‡πÄ‡∏ü‡∏£‡∏ä‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏î‡πâ‡∏ß‡∏¢‡∏ï‡∏ô‡πÄ‡∏≠‡∏á
  };
};